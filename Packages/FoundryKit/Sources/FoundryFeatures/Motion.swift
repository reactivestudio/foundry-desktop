import SwiftUI

/// Движение уровня ВСЕГО приложения — не привязано к онбордингу. Живёт на общем
/// уровне `FoundryFeatures` (рядом с `Token`), а не в неймспейсе фичи `OB`, чтобы
/// любой контрол приложения — в главном окне, консоли, будущих экранах — подчинялся
/// тем же законам движения из ОДНОГО источника. Онбординговые алиасы
/// `OB.easeReal` / `OB.hoverAnim` делегируют сюда.
enum AppMotion {
    /// Дом-кривая движения макета: cubic-bezier(0.2,0,0,1) — ease с сильным
    /// затуханием (быстрый старт, долгий мягкий доезд). База всех переходов.
    static func ease(_ duration: TimeInterval) -> Animation {
        .timingCurve(0.2, 0, 0, 1, duration: duration)
    }

    /// ЗАКОН ХОВЕРА (единый на всё приложение). Любые свойства, меняющиеся при
    /// наведении — цвет, тень, размер, отступы, сдвиги, прозрачность, что угодно —
    /// анимируются БЫСТРО на входе в ховер и ЗАМЕТНО МЕДЛЕННЕЕ, плавнее — на
    /// восстановлении при уходе курсора. Асимметрия: вход 0.14s, выход 0.70s (×5).
    ///
    /// Как применять: `.animation(AppMotion.hover(hovering), value: hovering)` —
    /// величина считается от НОВОГО состояния, поэтому true → быстро, false → медленно.
    static func hover(_ hovering: Bool) -> Animation {
        ease(hovering ? hoverIn : hoverOut)
    }

    /// Длительности закона ховера — единственная точка правки для всего приложения.
    static let hoverIn: TimeInterval = 0.14
    static let hoverOut: TimeInterval = 0.70
}
